public class IndicesCadenas {
    public static void main(String[] args) {
        var cadena1 = "Hola mundo";

        //recuerando carateres de una cadena
        var caracter1 = cadena1.charAt(0);
        System.out.println(caracter1);
        //recuperar el ultimo caracter
        var caracter2 = cadena1.charAt(9);
        System.out.println(caracter2);
        var letraM = cadena1.charAt(5);
        System.out.println(letraM);
    }
}
